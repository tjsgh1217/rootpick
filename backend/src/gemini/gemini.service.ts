  private async searchRealRestaurants(
    lat: number,
    lng: number,
  ): Promise<RestaurantData[]> {
    try {
      console.log('🔍 전국 동 단위 완전 세분화 검색 시작');
      console.log(`   좌표: ${lat}, ${lng}`);

      const searchQueries = this.getLocationBasedQuery(lat, lng);
      console.log(`🔍 검색 키워드들: ${searchQueries.join(', ')}`);

      let allRestaurants: RestaurantData[] = [];

      for (const query of searchQueries) {
        try {
          console.log(`🔍 "${query}" 검색 중...`);

          const response = await axios.get<NaverSearchResponse>(
            'https://openapi.naver.com/v1/search/local.json',
            {
              params: {
                query: query,
                display: 50,
                start: 1,
                sort: 'random',
              },
              headers: {
                'X-Naver-Client-Id': process.env.NAVER_CLIENT_ID,
                'X-Naver-Client-Secret': process.env.NAVER_CLIENT_SECRET,
              },
            },
          );

          console.log(
            `✅ "${query}" 검색 결과: ${response.data.items.length}개`,
          );

          if (response.data.items && response.data.items.length > 0) {
            const restaurantsWithDistance: RestaurantData[] =
              response.data.items
                .map((item) => {
                  const itemLat = parseFloat(item.mapy) / 10000000;
                  const itemLng = parseFloat(item.mapx) / 10000000;
                  const distance = this.calculateDistance(
                    lat,
                    lng,
                    itemLat,
                    itemLng,
                  );

                  console.log(
                    `📍 ${item.title.replace(/<[^>]*>/g, '')}: ${Math.round(distance)}m`,
                  );

                  return {
                    name: item.title.replace(/<[^>]*>/g, ''),
                    address: item.roadAddress || item.address,
                    telephone: item.telephone || '',
                    category: item.category,
                    lat: itemLat,
                    lng: itemLng,
                    distance: Math.round(distance),
                    link: item.link,
                    description: item.description || '',
                  };
                })
                .filter((restaurant) => restaurant.distance <= 5000);

            allRestaurants.push(...restaurantsWithDistance);
          }

          if (allRestaurants.length >= 50) {
            break;
          }

          await new Promise((resolve) => setTimeout(resolve, 100));
        } catch (queryError) {
          console.error(`❌ "${query}" 검색 실패:`, queryError.message);
          continue;
        }
      }

      const uniqueRestaurants = allRestaurants.filter(
        (restaurant, index, self) =>
          index ===
          self.findIndex(
            (r) =>
              r.name === restaurant.name && r.address === restaurant.address,
          ),
      );

      const closestRestaurants = uniqueRestaurants
        .sort((a, b) => a.distance - b.distance)
        .slice(0, 10);

      console.log(`✅ 가장 가까운 음식점 ${closestRestaurants.length}개 발견`);

      closestRestaurants.forEach((restaurant, index) => {
        const distanceKm =
          restaurant.distance >= 1000
            ? `${(restaurant.distance / 1000).toFixed(1)}km`
            : `${restaurant.distance}m`;
        console.log(`${index + 1}. ${restaurant.name} - ${distanceKm}`);
      });

      return closestRestaurants;
    } catch (error) {
      console.error('❌ 네이버 검색 API 오류:', error);
      throw new Error('네이버 API 호출에 실패했습니다.');
    }
  }

  private parseRealAddressInfo(
    address: string,
    roadAddress: string,
  ): {
    fullAddress: string;
    buildingInfo: string;
    floorInfo: string;
  } {
    const fullAddress = roadAddress || address;
    const buildingMatch = fullAddress.match(
      /([가-힣\w\s]+(?:빌딩|타워|센터|플라자|몰|마트|백화점|아파트))/,
    );
    const buildingInfo = buildingMatch ? buildingMatch[1] : '';
    const floorMatch = fullAddress.match(/(\d+층|지하\d+층|B\d+|지\d+층)/);
    const floorInfo = floorMatch ? floorMatch[1] : '';

    return {
      fullAddress,
      buildingInfo: buildingInfo || '',
      floorInfo: floorInfo || '',
    };
  }

  private formatActualPhoneNumber(telephone: string): string {
    if (!telephone || telephone === '정보 없음' || telephone.trim() === '') {
      return '';
    }

    const cleaned = telephone.replace(/[^\d]/g, '');
    if (cleaned.length === 10) {
      return `${cleaned.slice(0, 3)}-${cleaned.slice(3, 6)}-${cleaned.slice(6)}`;
    } else if (cleaned.length === 11) {
      return `${cleaned.slice(0, 3)}-${cleaned.slice(3, 7)}-${cleaned.slice(7)}`;
    }

    return telephone;
  }

  private calculateDistance(
    lat1: number,
    lng1: number,
    lat2: number,
    lng2: number,
  ): number {
    const R = 6371000;
    const dLat = ((lat2 - lat1) * Math.PI) / 180;
    const dLng = ((lng2 - lng1) * Math.PI) / 180;

    const a =
      Math.sin(dLat / 2) * Math.sin(dLat / 2) +
      Math.cos((lat1 * Math.PI) / 180) *
        Math.cos((lat2 * Math.PI) / 180) *
        Math.sin(dLng / 2) *
        Math.sin(dLng / 2);

    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
    return R * c;
  }

  private extractRealCuisineType(category: string): string {
    if (category.includes('한식')) return '한식';
    if (category.includes('중식')) return '중식';
    if (category.includes('일식')) return '일식';
    if (category.includes('양식')) return '양식';
    if (category.includes('치킨')) return '치킨';
    if (category.includes('피자')) return '피자';
    if (category.includes('카페')) return '카페';
    if (category.includes('분식')) return '분식';
    if (category.includes('베이커리')) return '베이커리';
    if (category.includes('디저트')) return '디저트';
    return category;
  }

  private extractAreaFromAddress(address: string): string {
    const match = address.match(/(.*?[시군구])/);
    return match ? match[1] : '';
  }

